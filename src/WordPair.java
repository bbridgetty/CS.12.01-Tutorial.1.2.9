import java.util.ArrayList;

public class WordPair {
    String first;
    String second;
    public WordPair (String first, String second){
        this.first = first; //linking the outside to inside (assigning a variable)
        this.second = second;
    }
    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
